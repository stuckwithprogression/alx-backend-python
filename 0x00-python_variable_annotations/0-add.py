#!/usr/bin/env python3
'''a type-annotated function that returns the sum of two floats"
'''


def add(a: float, b: float) -> float:
    '''get the sum of the two floats passed as arguments

    Args:
        a (float): first float
        b (float): second float

    Return:
        return the sum of a and b
    '''

    return a + b
